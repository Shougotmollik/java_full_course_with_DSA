package CoreJava;

public class squarePatternForLoop {
    public static void main(String[] args) {

//        For loop example
        for (int lines=1;lines<=4;lines++){
            System.out.println("* * * *");
        }

//        while loop example
    int line=1;
    while (line<=4){
        System.out.println("* * * *");
        line++;
    }
    }

}
