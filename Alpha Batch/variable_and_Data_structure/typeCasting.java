public class typeCasting {
    public static void main(String[] args) {
        float a =12.34f;
        int b = (int)a;
        System.out.println(b);

        // char to number

        char ch ='a';
        int number=ch;
        System.out.println(number);

        char ch2='A';
        int number2=ch2;
        System.out.println(number2);
    }
}
